package HelloWorld;
//Hexadecimal floating point literals
public class Lab057 {
public static void main(String[] args) {
	double d1=0XaP0;//a=10*2^0=10.0,
	double d2=0XaP1;//a=10*2^1=10*2=20;
	System.out.println(d1);
	System.out.println(d2);
	double d3=0XaP2;
	System.out.println(d3);//40.0
	double d4=0XA9.0P0;
	System.out.println(d4);//169.0
	double d5=0XA9.1P0;
	System.out.println(d5);//169.0
	
}
}
